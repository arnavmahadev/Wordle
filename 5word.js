    function checkGuess(guess, answer) {
        return guess.toUpperCase() === answer.toUpperCase();
    }
    
    function applyColorsToBoxes(guess, tempAnswer, boxes) {
        for (let i = 0; i < guess.length; i++) {
            let box = boxes[i];
            let letter = guess.charAt(i);
    
            if (tempAnswer[i] === letter) {
                box.style.backgroundColor = "green";
                tempAnswer[i] = null; 
            }
        }
    
        for (let i = 0; i < guess.length; i++) {
            let box = boxes[i];
            let letter = guess.charAt(i);
    
            if (tempAnswer.includes(letter) && tempAnswer[i] !== null) {
                box.style.backgroundColor = "yellow";
                tempAnswer[tempAnswer.indexOf(letter)] = null;
            }
        }
    
        for (let i = 0; i < guess.length; i++) {
            let box = boxes[i];
            if (box.style.backgroundColor === "") {
                box.style.backgroundColor = "gray";
            }
        }
    }
    
    
    
    document.addEventListener("keydown", (keypress) => {         //keydown from https://developer.mozilla.org/en-US/docs/Web/API/Element/keydown_event
        let pressedKey = keypress.key;           
        let activeBox = document.activeElement;
    
        if (activeBox && activeBox.classList.contains("box")) {
            if (pressedKey.length === 1) {
                let nextBox = activeBox.nextElementSibling;               // nextElementSibling from https://developer.mozilla.org/en-US/docs/Web/API/Element/nextElementSibling
                if (nextBox && nextBox.classList.contains("box")) {
                    nextBox.focus();
                }
                activeBox.textContent = pressedKey.toUpperCase();
                keypress.preventDefault();               // preventDefault from https://developer.mozilla.org/en-US/docs/Web/API/Event/preventDefault
            } else if (pressedKey === "Backspace") {
                activeBox.textContent = "";
                let prevBox = activeBox.previousElementSibling;            // previousElementSibling from https://developer.mozilla.org/en-US/docs/Web/API/Element/previousElementSibling
                if (prevBox && prevBox.classList.contains("box")) {
                    prevBox.focus();
                }
                keypress.preventDefault();
            } else if (pressedKey === "Enter" || pressedKey === "Return") {
                let row = activeBox.parentElement;
                let nextRow = row.nextElementSibling;
                let boxes = row.querySelectorAll(".box");
                let isFilled = true;
                for (let i = 0; i < boxes.length; i++) {
                    if (!boxes[i].textContent.trim()) {            // .trim() from https://www.w3schools.com/jsref/jsref_trim_string.asp
                        isFilled = false;
                        break;
                    }
                }
                if (isFilled) {
                    let guess = "";
                    for (let i = 0; i < boxes.length; i++) {
                        boxes[i].contentEditable = "false";
                        guess = guess + boxes[i].textContent;
                    }
    
                    applyColorsToBoxes(guess, answer.split(''), boxes);   // .split from https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Global_Objects/String/split
    
                    if (checkGuess(guess, answer)) {
                        alert("Congratulations! You've guessed the correct word: " + answer);
                        showReplayButton();
                    }
    
                    if (!nextRow) {
                        let allBoxesFilled = true;
                        document.querySelectorAll('.box').forEach(box => {
                            if (!box.textContent.trim()) {
                                allBoxesFilled = false;
                            }
                        });
                        if (allBoxesFilled && !checkGuess(guess, answer)) {
                            alert("Sorry, you've used up all your guesses. The correct answer is: " + answer);
                            showReplayButton();
                        }
                    } else {
                        let firstBoxNextRow = nextRow.querySelector(".box");
                        firstBoxNextRow.focus();
                    }
    
                    keypress.preventDefault();
                    return false;
                }
            }
        }
    });
    
